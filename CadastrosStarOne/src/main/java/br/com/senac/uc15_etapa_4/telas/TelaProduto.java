    private DefaultTableModel montarTabela(List<Material> listaMaterial) {
        String[] colunas = {"Código", "Descrição", "Código de Barras", "UMB"};
        DefaultTableModel tabela = new DefaultTableModel();
        tabela.setColumnIdentifiers(colunas);

        for (int i = 0; listaMaterial.size() > i; i++) {
            Material material = listaMaterial.get(i);

            String[] linha = {
                Integer.toString(material.getId()),
                material.getDescricao(),
                material.getCodigoBarras(),
                material.getUnidadeMedida(),
            };

            tabela.addRow(linha);
        }
        return tabela;
    }

    private String getIdItemSelecionado() {
        int posicao = jTableProduto.getSelectedRow();
        if (posicao == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um item da tabela!");
        }
        return (String) jTableProduto.getValueAt(posicao, 0);
    }

