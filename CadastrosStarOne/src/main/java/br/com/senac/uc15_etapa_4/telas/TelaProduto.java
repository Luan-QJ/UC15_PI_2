    private void jButtonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGravarActionPerformed
        // TODO add your handling code here:
        Material material = new Material();
        material.setCodigoBarras(ean.getText());
        material.setUnidadeMedida(umb.getText());
        material.setDescricao(descricao.getText());
        material.setUtilizacaoProdutoId(Integer.parseInt(utilizacao.getText()));
        material.setPrazoValidade(prazo.getText());
        material.setPesoBruto(pesoBruto.getText());
        material.setPesoLiquido(pesoLiquido.getText());
        material.setCategoriaProdutoId(Integer.parseInt(categoria.getText()));
        material.setFamiliaProdutoId(Integer.parseInt(familia.getText()));
        material.setCompradorProdutoId(Integer.parseInt(comprador.getText()));
        material.setFornecedorId(Integer.parseInt(fornecedor.getText()));
        material.setPrecoCompra(preco.getText());
        material.setStatusCadastroId(Integer.parseInt(status.getText()));
        material.setCriadoEm(criadoEm.getText());
        material.setCriadoPor(criadoPor.getText());
        material.setModificadoEm(modificadoEm.getText());

        if (materialEdicao != null) {

            materialEdicao.setCodigoBarras(ean.getText());
            materialEdicao.setUnidadeMedida(umb.getText());
            materialEdicao.setDescricao(descricao.getText());
            materialEdicao.setUtilizacaoProdutoId(Integer.parseInt(utilizacao.getText()));
            materialEdicao.setPrazoValidade(prazo.getText());
            materialEdicao.setPesoBruto(pesoBruto.getText());
            materialEdicao.setPesoLiquido(pesoLiquido.getText());
            materialEdicao.setCategoriaProdutoId(Integer.parseInt(categoria.getText()));
            materialEdicao.setFamiliaProdutoId(Integer.parseInt(familia.getText()));
            materialEdicao.setCompradorProdutoId(Integer.parseInt(comprador.getText()));
            materialEdicao.setFornecedorId(Integer.parseInt(fornecedor.getText()));
            materialEdicao.setPrecoCompra(preco.getText());
            materialEdicao.setStatusCadastroId(Integer.parseInt(status.getText()));
            materialEdicao.setCriadoEm(criadoEm.getText());
            materialEdicao.setCriadoPor(criadoPor.getText());
            materialEdicao.setModificadoEm(modificadoEm.getText());
            MaterialJPA.atualizar(materialEdicao);
            JOptionPane.showMessageDialog(null, "Produto atualizado com sucesso!");

        } else {
            MaterialJPA.cadastrar(material);
            JOptionPane.showMessageDialog(null, "Produto cadastrado com sucesso!");

        }
        jTableProduto.setModel(montarTabela(MaterialJPA.listar()));
        jScrollPane1.setViewportView(jTableProduto);

        //limpar os campos
        ean.setText("");
        umb.setText("");
        descricao.setText("");
        utilizacao.setText("");
        prazo.setText("");
        pesoBruto.setText("");
        pesoLiquido.setText("");
        categoria.setText("");
        familia.setText("");
        comprador.setText("");
        fornecedor.setText("");
        preco.setText("");
        status.setText("");
        criadoEm.setText("");
        criadoPor.setText("");
        modificadoEm.setText("");

        materialEdicao = null;
    }//GEN-LAST:event_jButtonGravarActionPerformed

