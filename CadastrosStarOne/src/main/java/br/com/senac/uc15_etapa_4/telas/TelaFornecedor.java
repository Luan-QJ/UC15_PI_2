    private DefaultTableModel montarTabela(List<Fornecedor> listaFornecedor) {
        String[] colunas = {"ID", "CNPJ", "Razão Social", "UF"};
        DefaultTableModel tabela = new DefaultTableModel();
        tabela.setColumnIdentifiers(colunas);

        for (int i = 0; listaFornecedor.size() > i; i++) {
            Fornecedor fornecedor = listaFornecedor.get(i);

            String[] linha = {
                Integer.toString(fornecedor.getId()),
                fornecedor.getCpfCnpj(),
                fornecedor.getRazaoSocial(),
                fornecedor.getUf(),
            };
            
            tabela.addRow(linha);
        }
        return tabela;
    }

    private String getIdItemSelecionado() {
        int posicao = jTableFornecedor.getSelectedRow();
        if (posicao == -1) {
            JOptionPane.showMessageDialog(null, "Selecione um item da tabela!");
        }
        return (String) jTableFornecedor.getValueAt(posicao, 0);
    }

