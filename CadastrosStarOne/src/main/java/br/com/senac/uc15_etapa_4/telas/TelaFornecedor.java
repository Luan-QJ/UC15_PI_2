    private void jButtonGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGravarActionPerformed
        // TODO add your handling code here:
        Fornecedor fornecedor = new Fornecedor();
        fornecedor.setCpfCnpj(cnpj.getText());
        fornecedor.setRazaoSocial(razao.getText());
        fornecedor.setUf(uf.getText());
        fornecedor.setEndereco(endereco.getText());
        fornecedor.setNumeroEndereco(numEndereco.getText());
        fornecedor.setCep(cep.getText());
        fornecedor.setBairro(bairro.getText());
        fornecedor.setCidade(cidade.getText());
        fornecedor.setEmail(email.getText());
        fornecedor.setTelefone(telefone.getText());
        fornecedor.setVendedor(vendedor.getText());
        fornecedor.setCondicaoPagamentoId(Integer.parseInt(condicao.getText()));
        fornecedor.setStatusCadastroId(Integer.parseInt(status.getText()));
        fornecedor.setBancoId(Integer.parseInt(banco.getText()));
        fornecedor.setAgencia(agencia.getText());
        fornecedor.setConta(conta.getText());
        fornecedor.setCriadoEm(criadoEm.getText());
        fornecedor.setCriadoPor(criadoPor.getText());
        fornecedor.setModificadoEm(modificadoEm.getText());

        if (fornecedorEdicao != null) {
            fornecedorEdicao.setCpfCnpj(cnpj.getText());
            fornecedorEdicao.setRazaoSocial(razao.getText());
            fornecedorEdicao.setUf(uf.getText());
            fornecedorEdicao.setEndereco(endereco.getText());
            fornecedorEdicao.setNumeroEndereco(numEndereco.getText());
            fornecedorEdicao.setCep(cep.getText());
            fornecedorEdicao.setBairro(bairro.getText());
            fornecedorEdicao.setCidade(cidade.getText());
            fornecedorEdicao.setEmail(email.getText());
            fornecedorEdicao.setTelefone(telefone.getText());
            fornecedorEdicao.setVendedor(vendedor.getText());
            fornecedorEdicao.setCondicaoPagamentoId(Integer.parseInt(condicao.getText()));
            fornecedorEdicao.setStatusCadastroId(Integer.parseInt(status.getText()));
            fornecedorEdicao.setBancoId(Integer.parseInt(banco.getText()));
            fornecedorEdicao.setAgencia(agencia.getText());
            fornecedorEdicao.setConta(conta.getText());
            fornecedorEdicao.setCriadoEm(criadoEm.getText());
            fornecedorEdicao.setCriadoPor(criadoPor.getText());
            fornecedorEdicao.setModificadoEm(modificadoEm.getText());
            FornecedorJPA.atualizar(fornecedorEdicao);
            JOptionPane.showMessageDialog(null, "Fornecedor atualizado com sucesso!");

        } else {
            FornecedorJPA.cadastrar(fornecedor);
            JOptionPane.showMessageDialog(null, "Fornecedor cadastrado com sucesso!");

        }
        jTableFornecedor.setModel(montarTabela(FornecedorJPA.listar()));
        jScrollPane1.setViewportView(jTableFornecedor);

        //limpar os campos
        cnpj.setText("");
        razao.setText("");
        uf.setText("");
        endereco.setText("");
        numEndereco.setText("");
        cep.setText("");
        bairro.setText("");
        cidade.setText("");
        email.setText("");
        telefone.setText("");
        vendedor.setText("");
        condicao.setText("");
        status.setText("");
        banco.setText("");
        agencia.setText("");
        conta.setText("");
        criadoEm.setText("");
        criadoPor.setText("");
        modificadoEm.setText("");

        fornecedorEdicao = null;
    }//GEN-LAST:event_jButtonGravarActionPerformed

